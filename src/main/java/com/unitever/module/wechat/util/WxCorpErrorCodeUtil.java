	/**
	 * 依据微信返回的错误码，返回错误码对应的错误描述，方便于页面显示
	 * 
	 * @param wxCorpErrorCodeVal
	 *            微信返回码code，int类型
	 * @return String 返回微信错误码对应的错误描述
	 */
	public static String getWxCorpErrorValue(int wxCorpErrorCodeVal) {
		String errorCode = "wxCorpErrorCode_" + wxCorpErrorCodeVal;
		// 变量名不可用包含'-' 这里判断错误码是否为'-1'，以便为'-1'的错误码返回其对应的错误描述（微信返回码中暂未发现有'1'的返回码）
		if ("-1".equals(wxCorpErrorCodeVal)) {
			errorCode = "wxCorpErrorCode_1";
		}
		try {
			return (String) WxCorpErrorCodeUtil.class.getField(errorCode).get(null);
		} catch (Exception e) {
			return "未识别的错误code，请联系管理员";
		}
	}
